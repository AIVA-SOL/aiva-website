/* ─────────────────────────────────────────────
   Dropdown language switcher engine
───────────────────────────────────────────── */
(function () {
  const LANGS = ['en', 'zh', 'zht', 'ja', 'ko'];
  const LANG_LABELS = {
    en:  '🌐 English',
    zh:  '🇨🇳 简体中文',
    zht: '🇹🇼 繁體中文',
    ja:  '🇯🇵 日本語',
    ko:  '🇰🇷 한국어',
  };
  const LANG_SHORT = { en:'EN', zh:'简中', zht:'繁中', ja:'日本語', ko:'한국어' };
  const LS_KEY = 'aiva_lang';

  function getLang() {
    const saved = localStorage.getItem(LS_KEY);
    // Validate saved value is one of the supported langs
    if (saved && ['en','zh','zht','ja','ko'].includes(saved)) return saved;
    // Default to English and save it
    localStorage.setItem(LS_KEY, 'en');
    return 'en';
  }

  function setLang(lang) {
    localStorage.setItem(LS_KEY, lang);
    applyLang(lang);
    // close all dropdowns
    document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.remove('open'));
  }

  function applyLang(lang) {
    document.documentElement.lang = lang === 'zht' ? 'zh-TW' : lang === 'zh' ? 'zh-CN' : lang;
    // Apply all data-i18n text nodes
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (AIVA_I18N[key]) {
        const val = AIVA_I18N[key][lang] || AIVA_I18N[key]['en'];
        if (val !== undefined) el.innerHTML = val;
      }
    });
    // Update all dropdown triggers
    document.querySelectorAll('.lang-trigger-label').forEach(el => {
      el.textContent = LANG_SHORT[lang] || 'EN';
    });
  }

  /* Build the dropdown language switcher */
  function buildDropdown(container) {
    if (!container) return;

    const wrap = document.createElement('div');
    wrap.className = 'lang-dropdown';

    const trigger = document.createElement('button');
    trigger.className = 'lang-trigger';
    trigger.innerHTML = `<span class="lang-trigger-label">${LANG_SHORT[getLang()]}</span><svg class="lang-caret" width="12" height="12" viewBox="0 0 12 12" fill="none"><path d="M2 4l4 4 4-4" stroke="currentColor" stroke-width="1.8" stroke-linecap="round" stroke-linejoin="round"/></svg>`;
    trigger.onclick = (e) => {
      e.stopPropagation();
      const isOpen = wrap.classList.contains('open');
      // close all other dropdowns
      document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.remove('open'));
      if (!isOpen) wrap.classList.add('open');
    };

    const menu = document.createElement('div');
    menu.className = 'lang-menu';

    LANGS.forEach(l => {
      const item = document.createElement('button');
      item.className = 'lang-item';
      item.dataset.lang = l;
      item.textContent = LANG_LABELS[l];
      item.onclick = (e) => { e.stopPropagation(); setLang(l); };
      menu.appendChild(item);
    });

    wrap.appendChild(trigger);
    wrap.appendChild(menu);
    container.appendChild(wrap);
  }

  /* Inject global CSS for the dropdown */
  function injectStyles() {
    if (document.getElementById('aiva-i18n-styles')) return;
    const style = document.createElement('style');
    style.id = 'aiva-i18n-styles';
    style.textContent = `
      /* ── Lang Dropdown ── */
      .lang-dropdown {
        position: relative;
        display: inline-block;
        flex-shrink: 0;
      }
      .lang-trigger {
        display: flex;
        align-items: center;
        gap: 5px;
        padding: 6px 13px;
        border-radius: 99px;
        border: 1px solid rgba(79,142,255,.28);
        background: rgba(79,142,255,.07);
        color: #a0b3d4;
        font-size: .75rem;
        font-weight: 700;
        cursor: pointer;
        transition: all .2s;
        white-space: nowrap;
        letter-spacing: .04em;
      }
      .lang-trigger:hover {
        border-color: rgba(79,142,255,.6);
        color: #4f8eff;
        background: rgba(79,142,255,.12);
      }
      .lang-caret {
        transition: transform .2s;
        color: currentColor;
      }
      .lang-dropdown.open .lang-caret {
        transform: rotate(180deg);
      }
      .lang-menu {
        position: absolute;
        top: calc(100% + 8px);
        right: 0;
        min-width: 160px;
        background: #0d1530;
        border: 1px solid rgba(79,142,255,.22);
        border-radius: 12px;
        padding: 6px;
        box-shadow: 0 16px 48px rgba(0,0,0,.55);
        display: none;
        z-index: 9999;
        flex-direction: column;
        gap: 2px;
      }
      .lang-dropdown.open .lang-menu {
        display: flex;
        flex-direction: column;
        animation: fadeDropdown .15s ease;
      }
      @keyframes fadeDropdown {
        from { opacity: 0; transform: translateY(-6px); }
        to   { opacity: 1; transform: translateY(0); }
      }
      .lang-item {
        display: block;
        width: 100%;
        text-align: left;
        padding: 8px 12px;
        border: none;
        background: transparent;
        color: #94a3b8;
        font-size: .82rem;
        font-weight: 500;
        cursor: pointer;
        border-radius: 8px;
        transition: all .15s;
        white-space: nowrap;
      }
      .lang-item:hover {
        background: rgba(79,142,255,.12);
        color: #e2e8f0;
      }
      .lang-item.active {
        color: #4f8eff;
        background: rgba(79,142,255,.1);
        font-weight: 700;
      }
      @media (max-width: 480px) {
        .lang-menu { right: auto; left: 0; }
      }
    `;
    document.head.appendChild(style);
  }

  /* Update active state in open menus */
  function updateActiveItems(lang) {
    document.querySelectorAll('.lang-item').forEach(item => {
      item.classList.toggle('active', item.dataset.lang === lang);
    });
  }

  /* Close dropdown when clicking outside */
  document.addEventListener('click', () => {
    document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.remove('open'));
  });

  /* Run on DOM ready */
  document.addEventListener('DOMContentLoaded', () => {
    injectStyles();
    document.querySelectorAll('.lang-switcher-slot').forEach(slot => {
      buildDropdown(slot);
    });
    const lang = getLang();
    applyLang(lang);
    updateActiveItems(lang);
  });

  // Re-expose for external calls (whitepaper page etc.)
  window.AIVAi18n = {
    setLang: (l) => { setLang(l); updateActiveItems(l); },
    getLang,
    applyLang,
    LANGS,
    LANG_LABELS,
  };
})();
